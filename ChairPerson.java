public class ChairPerson {
    int age;
    String name;
    String surName;
    String gender;
    String address;

    ChairPerson(String name, String surName, int age, String gender, String address) {
        this.name = name;
        this.surName = surName;
        this.age = age;
        this.gender = gender;
        this.address = address;
    }
}
